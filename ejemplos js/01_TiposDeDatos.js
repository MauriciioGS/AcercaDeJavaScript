/**
 * Tipos de datos
 */

//numero - number

console.log(27);
console.log(27.56);

//texto - string
console.log("Gabriel Arguello");
console.log("Pamela Espinoza");
console.log(`hola`);

//logicos - booleanos
console.log(true);
console.log(false);

//indefinido - undefined
console.log(undefined);

//null
console.log(null);

/**
 * Tipos de datos estructurales
 */

//objeto - object
//clave: valor
console.log({ nombre: "Pamela", edad: 23, fechaNacimiento: null });

//tipos de datos de tipo lista - array
console.log(["Mexico", { nombre: "Pamela" }, [1, 2, 3, 4]]);
